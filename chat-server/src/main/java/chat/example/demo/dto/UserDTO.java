package chat.example.demo.dto;

import chat.example.demo.entity.User;
import jakarta.persistence.Column;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import lombok.AllArgsConstructor;
import lombok.Data;

@Data
@AllArgsConstructor
public class UserDTO {

    private long userId;

    private String userEmail;

    private String userPassword;

    private String userName;

    private String userPhoneNumber;

    private String userZipCode;

    private String userAddress;

    private String userDetailAddress;

    public static UserDTO translate(User user){
        return new UserDTO(user.getUserId(), user.getUserEmail(), user.getUserPassword(), user.getUsername(),
                user.getUserPhoneNumber(), user.getUserZipCode(), user.getUserAddress(), user.getUserDetailAddress());
    }
}
