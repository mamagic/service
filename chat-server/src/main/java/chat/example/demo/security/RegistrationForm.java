package chat.example.demo.security;

import chat.example.demo.dto.UserDTO;
import chat.example.demo.entity.User;
import lombok.Data;
import org.springframework.security.crypto.password.PasswordEncoder;

@Data
public class RegistrationForm {

  public static User toUser(PasswordEncoder passwordEncoder, UserDTO userDTO) {
    return new User(userDTO.getUserId(),
        userDTO.getUserEmail(), passwordEncoder.encode(userDTO.getUserPassword()),userDTO.getUserName(),
            userDTO.getUserPhoneNumber(), userDTO.getUserZipCode(), userDTO.getUserAddress(), userDTO.getUserDetailAddress());
  }

}
